function switchVisible() {
            if (document.getElementById('box1h')) {

                if (document.getElementById('box1h').style.display == 'none') {
                    document.getElementById('box1h').style.display = 'block';
                    document.getElementById('box3h').style.display = 'none';
                    document.getElementById('box4h').style.display = 'none';
                    document.getElementById('box5h').style.display = 'none';
                    document.getElementById('box6h').style.display = 'none';
                    document.getElementById('box2h').style.display = 'none';
                }
                else {
                    document.getElementById('box1h').style.display = 'block';
                    document.getElementById('box2h').style.display = 'none';
                    document.getElementById('box3h').style.display = 'none';
                    document.getElementById('box4h').style.display = 'none';
                    document.getElementById('box5h').style.display = 'none';
                    document.getElementById('box6h').style.display = 'none';
                }
            }
}
