function switchVisibleadmin2() {
            if (document.getElementById('dbox2')) {

                if (document.getElementById('dbox2').style.display == 'none') {
					document.getElementById('dbox1').style.display = 'none';
                    document.getElementById('dbox2').style.display = 'block';
                    document.getElementById('dbox3').style.display = 'none';
                    document.getElementById('dbox4').style.display = 'none';
                    document.getElementById('dbox5').style.display = 'none';
                    document.getElementById('dbox6').style.display = 'none';
                    document.getElementById('dbox7').style.display = 'none';
                }
                else {
					document.getElementById('dbox1').style.display = 'none';
                    document.getElementById('dbox2').style.display = 'block';
                    document.getElementById('dbox3').style.display = 'none';
                    document.getElementById('dbox4').style.display = 'none';
                    document.getElementById('dbox5').style.display = 'none';
                    document.getElementById('dbox6').style.display = 'none';
                    document.getElementById('dbox7').style.display = 'none';
                }
            }
}
